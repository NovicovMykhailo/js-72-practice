// ## Example 1 - Математические операторы

// Выведи на экран общее количество яблок и винограда. Разницу яблок и винограда.


const apples = 47;
const grapes = 135;

console.log("Количество яблок: " + apples)
console.log("Количество винограда: " + grapes)
const total = apples + grapes;
console.log("Общее количество яблок и винограда: " + total)
const diff = grapes - apples;
console.log("Разницу яблок и винограда: " + diff)
